######## function to make and save bar charts for a single year ########
def make_bar_chart_single_year(metric, year):
	# Ensure output directories exist
	Path("Result Images").mkdir(exist_ok=True)
	Path(f"Result Images/Bar Charts").mkdir(exist_ok=True)
	# Handle input differences by metric
	if metric == "TD":
		plot_col = 'Turning Density [deg/km]'
		title_str = f'Turning Density (TD) Ranking of {year} Formula 1 Circuits'
		xlabel_str = 'Degrees Turned per Kilometer of Track [°/km]'
		units_str = '°/km'
		annotation_pad = 50
	elif metric == "LGWTD":
		plot_col = 'Lateral G-Weighted Turning Density [G-deg/km]'
		title_str = f'Lateral G-Weighted Turning Density (LGWTD) Ranking of {year} Formula 1 Circuits'
		xlabel_str = 'Lateral G-Weighted Turning Density [G-°/km]'
		units_str = ' G-°/km'
		annotation_pad = 125
	else:
		print(f'ERROR: Metric {metric} is undefined: options are TD or LGWTD')
		return
	# read data from CSV generated by compute_TD_and_LGWTD.py
	try:
		df_metric_data = pd.read_csv(f'Result CSVs/{year}_F1_TD_and_LGWTD.csv')
	except FileNotFoundError as e:
		print(f"ERROR reading file: {e}")
		return
	
	# Rank order by requested metric
	df_metric_data['Rank'] = df_metric_data[plot_col].rank(ascending=False, method='first').astype(int)
	df_metric_data = df_metric_data.sort_values(by='Rank').reset_index(drop=True)
	# Classify tracks
	# Note: These only currently consider 2024-2026 circuits
	non_permanent_circuits = [
		# Full street circuits
		'Jeddah', 'Monte Carlo', 'Monaco', 'Baku', 'Marina Bay', 'Las Vegas',
		# Temporary/semi-permanent street-style circuits
		'Melbourne', 'Miami Gardens', 'Miami', 'Montréal', 'Madrid'
	]
	# Create a classification column
	df_metric_data['Circuit Type'] = df_metric_data['Circuit'].apply(
		lambda x: 'Non-Permanent' if x in non_permanent_circuits else 'Permanent'
	)
	# Determine color configuration based on metric type
	if metric == "LGWTD":
        # Map values to custom colors for track conditions
		color_palette = {'Dry': '#e10600', 'Wet': '#0066ff'}
		hue_col = 'Track Condition'
	else:
        # Fall back to default Circuit Type mapping
		color_palette = {'Non-Permanent': '#2b2b2b', 'Permanent': '#e10600'}
		hue_col = 'Circuit Type'
	# Create the horizontal bar plot
	sns.set_theme(style="darkgrid")
	plt.figure(figsize=(12, 8))
	ax = sns.barplot(
		x=plot_col,
		y='Circuit',
		hue=hue_col,
		data=df_metric_data,
		palette=color_palette,
		dodge=False,
		order=df_metric_data['Circuit']
	)
	# ensure annotations stay within axes limits
	ax.set_xlim(0, np.ceil((df_metric_data[plot_col].max()+annotation_pad)/annotation_pad)*annotation_pad)
	# Stylize the chart
	plt.title(title_str, fontsize=16, fontweight='bold', pad=20)
	plt.xlabel(xlabel_str, fontsize=14, labelpad=10)
	plt.ylabel('Grand Prix Circuit', fontsize=14)
	plt.legend(title=hue_col, loc='lower right', frameon=True)
	# Add data value labels to the end of each bar
	for p in ax.patches:
		width = p.get_width()
		if width > 0: # Ensure valid bars are labeled
			ax.text(
				width + df_metric_data[plot_col].max() * 0.01, # Position slightly past the end of the bar
				p.get_y() + p.get_height() / 2, # Center vertically in the bar row
				f'{width:.1f}{units_str}',         
				va='center',
				fontsize=10,
				fontweight='bold'
			)
	plt.tight_layout()
	# Save the plot
	plt.savefig(f'Result Images/Bar Charts/{year}_F1_{metric}_bar_chart.png', dpi=450, bbox_inches="tight")
	plt.close()
	# plt.show()

